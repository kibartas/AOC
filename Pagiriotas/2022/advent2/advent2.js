function readGames2() {
  const lines = input.split("\n");
  for (let i = 0; i < lines.length; i++) {
    const moves = lines[i].split(" ");
    const game = {
      move1: moves[0],
      move2: guessMove(moves[1], moves[0]),
    };
    games.push(game);
  }
}

function translateMove(move) {
  if (move === "X") return "A";
  if (move === "Y") return "B";
  if (move === "Z") return "C";
}

function guessMove(outcome, opponentMove) {
  if (outcome === "X") {
    if (opponentMove === "A") return "C";
    if (opponentMove === "B") return "A";
    if (opponentMove === "C") return "B";
  }
  if (outcome === "Y") return opponentMove;
  if (outcome === "Z") {
    if (opponentMove === "A") return "B";
    if (opponentMove === "B") return "C";
    if (opponentMove === "C") return "A";
  }
}

function getPointsForMove(move) {
  if (move === "A") return 1;
  if (move === "B") return 2;
  if (move === "C") return 3;
}

function getPointsForOutcome(game) {
  const { move1, move2 } = game;
  if (move1 === move2) return 3;
  else if (
    (move1 === "A" && move2 === "B") ||
    (move1 === "B" && move2 === "C") ||
    (move1 === "C" && move2 === "A")
  )
    return 6;
  return 0;
}

function getPointsForGame(game) {
  return getPointsForOutcome(game) + getPointsForMove(game.move2);
}

function calculatePoints() {
  let points = 0;
  games.forEach((game) => {
    // console.log(getPointsForGame(game));
    points += getPointsForGame(game);
  });
  return points;
}

